// typeof operator

// data types (primitive data types)
// string "Nirob"
// number 2,4,6
// booleans
// undefined
// null
// BigInt
// Symbol 

let age = 22;
let firstName = "Nirob";

console.log(typeof firstName);



// convert number to string
console.log(typeof (age + ""));


// convert string to number
let myst = +"34";
console.log(typeof myst);


// convert number to string
let yr = 18;
yr = String(yr);
console.log(typeof yr);


// convert string to Number
let yar = 18;
yar = Number(yar);
console.log(typeof yar);