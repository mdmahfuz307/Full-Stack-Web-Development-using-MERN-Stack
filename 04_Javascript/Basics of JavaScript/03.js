// rules for naming variables

// you cannot start with number
// example :-
// 1value(invalid)
// value1 (valid)

var value1 = 10;
console.log(value1);

// you can only undersore - or dollar symbol
// first_name (valid)
// _firstname(valid)

var _name = "Nirob";
console.log(_name);

// first$name (valid)
// $firstname (valid)

var first$name = "Shanto";
console.log(first$name);

